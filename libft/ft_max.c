#include "libft.h"

int		ft_max(int *tab, int length)
{
	int	i;
	int	max;

	i = 0;
	max = 0;
	while (i < length)
	{
		if (i == 0)
			max = i;
		if (*(tab + i) > max)
			max = *(tab + i);
		i++;
	}
	return (max);
}
